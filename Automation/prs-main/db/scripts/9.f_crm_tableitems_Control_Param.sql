TABLE: PRS_CONTROL_PARAM
---------------------------------------------------------------------- */
CREATE TABLE PRSDBM.PRS_CONTROL_PARAM
(
	PARAM_N VARCHAR2(11) NOT NULL,
    PARAM_NM VARCHAR2(50) NOT NULL,
    PARAM_VAL_T VARCHAR2(50) NOT NULL,
    PARAM_DESC_T VARCHAR2(100),
    START_DTM DATE,
    END_DTM DATE
)
;

