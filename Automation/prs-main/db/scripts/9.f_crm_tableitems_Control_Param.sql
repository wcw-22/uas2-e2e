COMMENT ON COLUMN PRSDBM.PRS_CONTROL_PARAM.PARAM_N IS 'Primary key of record. Generated by Oracle sequence.';
COMMENT ON COLUMN PRSDBM.PRS_CONTROL_PARAM.PARAM_NM IS 'Name of the parameter.';
COMMENT ON COLUMN PRSDBM.PRS_CONTROL_PARAM.PARAM_VAL_T IS 'Value of the parameter.';
COMMENT ON COLUMN PRSDBM.PRS_CONTROL_PARAM.PARAM_DESC_T IS 'Description of the parameter.';
COMMENT ON COLUMN PRSDBM.PRS_CONTROL_PARAM.START_DTM IS 'Effective start date of the parameter.';
COMMENT ON COLUMN PRSDBM.PRS_CONTROL_PARAM.END_DTM IS 'Effective end date of the parameter.';


